# Installing a package using apt
# Replace 'package-name' with the actual package (e.g., curl, vim, etc.)
sudo apt install package-name

# Upgrading a package
# Replace 'package-name' with the actual package name
sudo apt upgrade package-name

# Removing a package using apt
sudo apt remove package-name

# Removing a package along with its configuration files
sudo apt purge package-name

# Updating the package list
sudo apt update

